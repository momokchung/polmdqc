#include "tetrahedron.h"

/* ===========================================================================================
   Constructor
 ==============================================================================================*/

Tetrahedron::Tetrahedron() {
	for (int i = 0; i < 4; i++) {
		this->Vertices[i] = 0;
		this->Neighbours[i] = 0;
		this->nindex[i] = 0;
	}
	std::bitset<8> b(std::string("00000000"));
	this->info = b;
}

/* ===========================================================================================
   Initialize
 ==============================================================================================*/

void Tetrahedron::init() {
	for (int i = 0; i < 4; i++) {
		this->Vertices[i] = -1;
		this->Neighbours[i] = -1;
		this->nindex[i] = -1;
	}
	std::bitset<8> b(std::string("00000000"));
	this->info = b;
}

/* ===========================================================================================
   Destructor
 ==============================================================================================*/

Tetrahedron::~Tetrahedron() {
}
